package Lessons;

import java.util.Scanner;

public class doWhile {
    public static void main(String[] args) {
        int b = 0;
        while (b < 10) {
            b++;
            System.out.println("Пельмень №" + b);
            if (b == 4) {
                System.out.println("Вот счастливый пельмень");
                break; }
            }
        }
    }
